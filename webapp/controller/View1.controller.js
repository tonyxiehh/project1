sap.ui.define(["sap/ui/core/mvc/Controller"], function(Controller) {
	"use strict";
	var oView;
	return Controller.extend("ZPP.controller.View1", {
		/**
		 *@memberOf ZPP.controller.View1
		 */
		onInit: function() {
			oView = this.getView();
		},
		onPressSet: function(oEvent) {
			debugger;
			//          Input Field
			oView.byId("__input0").setVisible(true);
			oView.byId("__input0").setValue("Fuck Text");
			//          RadioButton
			oView.byId("__button1").setSelected(true);
			//ComboBox
			var oListItem1 = new sap.ui.core.ListItem("idList1", {
				key: "Item5",
				text: "HTML5",
				additionalText: "Hyper Text Markup "
			});
			oView.byId("__box0").addItem(oListItem1);
			debugger;
			oView.byId("__box0").setValue("HTML5");
		},
		onPressGet: function(oEvent) {
			var lv = oView.byId("__input0").getValue();
			oView.byId("__input0").setVisible(false);
			sap.m.MessageToast.show(lv);
			alert(lv);
			//          RadioButton
			lv = oView.byId("__button1").getSelected();
			alert(lv);
			//          ComboBox
			lv = oView.byId("__box0").getValue();
			alert(lv);
		},
		/**
		 *@memberOf ZPP.controller.View1
		 */
		onSelectItem: function(oEvent) {
			//This code was generated by the layout editor.
			//way1 : 通过事件取得  
			var selText = oEvent.getParameter("selectedItem").getText();
			var selKey = oEvent.getParameter("selectedItem").getKey();
			//Way2 : 通过View控件取得 ,Get selected value without using event parameter
			var selectedText = this.byId("__box0").getSelectedItem().getText();
			var selectedKey = this.byId("__box0").getSelectedItem().getKey();
			alert("alert from" + selText + selKey);
		},
		/**
		 *@memberOf ZPP.controller.View1
		 */
		onError: function(oEvent) {
			//This code was generated by the layout editor.
			var oInputControl = oEvent.getSource();
			oInputControl.setValueState(sap.ui.core.ValueState.Error);
		},
		/**
		 *@memberOf ZPP.controller.View1
		 */
		onSuccess: function(oEvent) {
			//This code was generated by the layout editor.
			var oInputControl = oEvent.getSource();
			oInputControl.setValueState(sap.ui.core.ValueState.Success);
		},
		/**
		 *@memberOf ZPP.controller.View1
		 */
		onhandleUserInput: function(oEvent) {
			//This code was generated by the layout editor.
			var sUserInput = oEvent.getParameter("value");
			var oInputControl = oEvent.getSource();
			if (sUserInput) {
				oInputControl.setValueState(sap.ui.core.ValueState.Success);
			} else {
				oInputControl.setValueState(sap.ui.core.ValueState.Error);
			}
		},
		/**
		 *@memberOf ZPP.controller.View1
		 */
		onChange: function(oEvent) {
			//This code was generated by the layout editor.
			debugger;
			alert("alert from" + oEvent.mParameters.newValue);
		},
		/**
		 *@memberOf ZPP.controller.View1
		 */
		setRowEditable: function(edit, rowindex) {
			var model = this.getModel();
			var rowPath = this.getBindingInfo('rows').path;
			var rows = model.getProperty(rowPath);
			for (var i = 0; i < rows.length; i++) {
				var row = rows[i];
				if (i === rowindex) {
					row[edit] = true; // make the selected row editable and rest non editable
				} else {
					row[edit] = false;
				}
			}
			this.invalidate();
		},
		onSelectedChange: function(oEvent) {
			//This code was generated by the layout editor.
			var selectedindex = oEvent.getParameter("rowIndex"); // selected Row Index
			//var oView = this.getView();
		    var oTable = this.byId("__table0");                       
			// Set Selected Row Editable
			oTable.setRowEditable('edit', selectedindex); // 'edit' is feild name to which editable property is bound

		}
	});
});